async function main() {
  const workbook = new ExcelJS.Workbook()
  await workbook.xlsx.readFile(sourcePath)

  const sheet = workbook.getWorksheet('Product Upload')
  if (!sheet) {
    throw new Error('Product Upload sheet was not found in the source workbook.')
  }

  const headers = sheet.getRow(1).values.slice(1).map((value) => String(value ?? '').trim())
  const visibleColumnCount = headers.filter(Boolean).length

  for (let rowIndex = 2; rowIndex <= 201; rowIndex += 1) {
    for (let columnIndex = 1; columnIndex <= visibleColumnCount; columnIndex += 1) {
      sheet.getCell(rowIndex, columnIndex).value = ''
    }
  }

  rows.forEach((row, index) => {
    const worksheetRow = sheet.getRow(index + 2)
    headers.forEach((header, columnIndex) => {
      worksheetRow.getCell(columnIndex + 1).value = row[header] ?? ''
    })
    worksheetRow.commit()
  })

  await workbook.xlsx.writeFile(outputPath)
  console.log(outputPath)
}

main().catch((error) => {
  console.error(error)
  process.exit(1)
})
